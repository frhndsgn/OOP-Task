public class switchCase {

    public static void main(String[] args) {
        int pilih = 3;
        switch (pilih) {
            case 1:
                System.out.println("Hobi saya membaca");
                break;
            case 2:
                System.out.println("Hobi saya melukis");
                break;
            case 3:
                System.out.println("Hobi saya travelling");
                break;
        }
    }
}


